//STRING LENGTH

/**
 * To get the length of a string, use the length() function
 * 
 */ 

#include<iostream>

using namespace std;

int main (void)
{
    string Player_ = "JMPAU";
    cout << " * Name : " << Player_ << endl;
    cout << " * Name length: " << Player_.length() << endl;
}

/**
 * Output:
 * Name : JMPAU
 * Name length: 5
*/